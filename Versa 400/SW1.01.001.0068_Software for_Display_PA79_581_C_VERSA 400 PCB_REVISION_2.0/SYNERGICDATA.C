unsigned int findmatindex(unsigned int wirespeed)
{
	unsigned int ans;
	//ans=mmmatgas[mm][mat][gas];	
	if(mat != sifcaw)
	{
		ans=mmmatgas[dia][mat][gas];	
	}
	else
	{
		if((gas == dcen)&&(dia == onepttwo))
		{
			ans = 55;		
		}
		else if ((gas == dcen)&&(dia == oneptsix))
		{
			ans = 56;		
		}
		else if ((gas == dcen)&&(dia == twoptzero))
		{
			ans = 57;		
		}
		else if((gas == dcep)&&(dia == onepttwo))
		{
			ans = 58;		
		}
		else if ((gas == dcep)&&(dia == oneptsix))
		{
			ans = 59;		
		}
		else if ((gas == dcep)&&(dia == twoptzero))
		{
			ans = 60;		
		}				
	}		
	program_number=ans;
	ans=autocurrent[ans][wirespeed];//16
	return(ans);
}
